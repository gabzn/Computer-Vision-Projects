def inbounds(shape, indices):
    assert len(shape) == len(indices)
    for i, ind in enumerate(indices):
        if ind < 0 or ind >= shape[i]:
            return False
    return True

def gaussian_blur_kernel_2d(sigma, height, width):
    '''Return a Gaussian blur kernel of the given dimensions and with the given
    sigma. Note that width and height are different.

    Input:
        sigma:  The parameter that controls the radius of the Gaussian blur.
                Note that, in our case, it is a circular Gaussian (symmetric
                across height and width).
        width:  The width of the kernel.
        height: The height of the kernel.

    Output:
        Return a kernel of dimensions height x width such that convolving it
        with an image results in a Gaussian-blurred image.
    '''
    # TODO-BLOCK-BEGIN
    gaussian_kernel = np.zeros((height, width), dtype=np.float32)
    center_x = height // 2
    center_y = width // 2
    total_sum = 0.0

    for i in range(height):
        for j in range(width):
            x = i - center_x
            y = j - center_y
            
            left_side = 1 / (2 * math.pi * pow(sigma, 2))

            exponent = -((pow(x, 2) + pow(y, 2)) / (2 * pow(sigma, 2)))
            right_side = math.exp(exponent)

            result = left_side * right_side
            gaussian_kernel[i, j] = result
            
            total_sum += result            
    
    gaussian_kernel /= total_sum
    return gaussian_kernel

